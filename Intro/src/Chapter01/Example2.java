package Chapter01;

public class Example2 {

	public static void main(String[] args) {
	// pokazanie kilku komunikatow oraz komentarze
		
		System.out.println("Witaj, świecie!");  //Pierwsza linia
		System.out.println("Jak się masz?");    //Druga linia
		
		// brak przeniesienia do drugiej linii
		
		System.out.print("Witaj, świecie! ");  //Pierwsza linia
		System.out.println("Jak się masz?");    //Druga linia
		
		//sekwencje ucieczki - \n
		
		System.out.print("Witaj!\nJak się masz?\nnowa linia\n");
		
		// sekwencja ucieczki i wyswietlenie ""
		
		System.out.println("Powiedziała mi \"Cześć!\".");
		
		// inne sekwencje ucieczki to \t - tabulacja
		// \\ ukosnik: \
		

	}

}
